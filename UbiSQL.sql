);

CREATE TABLE SPACES(
ID INT NOT NULL PRIMARY KEY, 
NAME VARCHAR(40),
ROOM_LENGTH FLOAT,
WIDTH FLOAT,
HEIGHT FLOAT,
ZONEID INT
);

CREATE TABLE ZONES(
ID INT NOT NULL PRIMARY KEY, 
DESCRIPTION VARCHAR(40),
BEGINX FLOAT,
ENDX FLOAT,
BEGINY FLOAT,
ENDY FLOAT,
BEGINZ FLOAT,
ENDZ FLOAT,
VID VARCHAR(40)
);

